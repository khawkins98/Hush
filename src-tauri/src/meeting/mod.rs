//! Meeting Mode â€” passive transcription of meeting / multi-speaker
//! audio sessions.
//!
//! Concept inspired by VoiceInk's writing-mode plus typical meeting-
//! transcript tools. Reimplemented from observed public behaviour and
//! the design memo at `docs/system-audio-meeting-mode-proposal.md`;
//! no source code referenced. See Â§13.8 of the PRD.
//!
//! ## Module layout
//!
//! - **`mod.rs`** â€” shared types: `MeetingAppKind`, `MeetingSession`,
//!   `PersistedUtterance`, the `MeetingSessionRepository` trait.
//! - **`manager`** â€” `SessionManager` runtime: lifecycle state
//!   machine, chunking pump, `AppClassifier` integration.
//!   Auto-start is driven by the CoreAudio HAL detection task
//!   in `lib.rs`; per-app policy overrides are tracked in #112.
//! - **`app_overrides`** â€” per-app classifier override storage
//!   (#112/#192). The Settings panel reads/writes through here;
//!   `SessionManager` reads a fresh snapshot at every session
//!   start so edits take effect without a restart.
//! - **`sqlite`** â€” `SqliteMeetingSessionRepository` persistence.
//!
//! Streaming inference + diarization compose externally:
//! `Transcribe::start_stream` (#108) feeds the pump; `Diarize`
//! (#191/#201) labels the finals before dispatch.
//!
//! ## Privacy invariant
//!
//! Only transcripts and timestamps land here. Raw audio bytes are
//! never persisted â€” they live in a RAM ring buffer during
//! inference and are discarded once each window has been
//! transcribed. The trait shape enforces this by accepting
//! `Utterance`s (text + timestamps) rather than audio buffers as
//! the input shape â€” there's no way to insert a `Vec<f32>` through
//! the data layer's API surface.

